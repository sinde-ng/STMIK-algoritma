public class TUGAS_Perulangan_No2 {
    
    public static void main(String[] args){
    
        int i = 7; // variabel
        
            do {
                
                System.out.println(i);
                i--; //decrement / pengurangan (7, 7-1=6, 7-2=5, 7-3=4, 7-4=3, 7-5=2, 7-6=1)
                
            }
            
            while(i >= 1); // batas perulangan
        
    }
}
