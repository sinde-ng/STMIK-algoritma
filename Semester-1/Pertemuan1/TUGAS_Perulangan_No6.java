public class TUGAS_Perulangan_No6 {
    
    public static void main(String[] args){
    
        int i = 3;
        
            do { 
            
                System.out.println(i);
                
                i+=3; // increment / menambahkan dengan kelipatan 3 (3, 3+3=6, 3+6=9, 3+9=12, 3+12=15, 3+15=18)
                
            }
            
            while (i <= 18); // batas perulangan
        
    }
    
}
