public class MATERI_For_Decrement {
    
    public static void main(String[] args){
        
        int i; // variabel
        
        for (
                i=10; // variabel diisi angka 10
                i>=1; // batas perulangan
                i-- // decrement/mengurangi (10, 10-1=9, 10-2=8, 10-3=7, 10-4=6, 10-7=3, 10-8=2, 10-9=1)
            ){
        
            System.out.println(i);
            
        }
        
    }
    
}
