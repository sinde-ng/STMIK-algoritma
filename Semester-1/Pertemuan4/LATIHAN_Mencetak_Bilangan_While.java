public class LATIHAN_Mencetak_Bilangan_While {
    
    public static void main(String[] args){
        
        int i = 1,jumlah = 0;
        System.out.println("Mencetak Bilangan Dengan While");
        
        while (i <= 10){
            
            jumlah = jumlah+i;
            
            System.out.println(i);
            i++;
        }
        
        System.out.println("Jumlah : "+jumlah);
        
    }
    
}

