public class MATERI_While {
    
    public static void main(String[] args){
        
        int i = 1;
        
        
        while(i<=5)//batas perulangan
        {
            
            System.out.println(i);
            i++; // increment / menambahkan (1, 1+1=2, 1+2=3, 1+3=4, 1+4=5)
            
        }
        
    }
    
}
